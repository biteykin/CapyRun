export function detectMultiWorkoutQuestionKind(text: string): MultiWorkoutQuestionKind {
  const t = normalizeText(text);
  if (!t) return "unknown";

  if (hasAny(t, FACTUAL_SUMMARY_MARKERS)) return "factual_summary";
  if (
    (t.includes("километраж") || t.includes("пробег") || t.includes("километров")) &&
    detectRequestedWindow(t) !== null
  ) {
    return "factual_summary";
  }
  if (
    (t.includes("виды спорта") ||
      t.includes("типы тренировок") ||
      t.includes("виды активности") ||
      t.includes("какие активности")) &&
    (t.includes("трениров") || t.includes("спорт") || t.includes("активност"))
  ) {
    return "factual_summary";
  }
  if (hasAny(t, PROGRESS_MARKERS)) return "progress_analysis";
  if (hasAny(t, TREND_MARKERS)) return "trend_analysis";

  return "unknown";
}

export function isFactualMultiWorkoutIntent(text: string) {
  const t = normalizeText(text);
  const kind = detectMultiWorkoutQuestionKind(text);
  return (
    kind === "factual_summary" ||
    (kind === "unknown" && (t.includes("виды спорта") || t.includes("по каким видам спорта") || t.includes("типы тренировок")))
  );
}

export function isMultiWorkoutAnalysisIntent(text: string) {
  const t = normalizeText(text);
  if (!t) return false;

  const hasBroad = hasAny(t, MULTI_WORKOUT_BROAD_MARKERS);
  const hasExplicitMulti = hasAny(t, MULTI_WORKOUT_EXPLICIT_MARKERS);
  const hasDetectedWindow = detectRequestedWindow(t) !== null;
  const hasDetectedKind = detectMultiWorkoutQuestionKind(t) !== "unknown";

  const hasPluralTrainingContext =
    (t.includes("тренировк") || t.includes("пробежк") || t.includes("бег")) &&
    (t.includes("последн") ||
      t.includes("нескольк") ||
      t.includes("недел") ||
      t.includes("месяц") ||
      t.includes("динамик") ||
      t.includes("прогресс") ||
      t.includes("в целом") ||
      t.includes("объем") ||
      t.includes("объём") ||
      t.includes("километраж") ||
      t.includes("пульс") ||
      t.includes("темп") ||
      t.includes("чсс"));

  return hasBroad || hasExplicitMulti || hasDetectedWindow || hasDetectedKind || hasPluralTrainingContext;
}

